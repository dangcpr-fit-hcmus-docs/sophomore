function [] = Lagrange7(xx,yy,xc)
    syms x
    L06=((x-xx(2))*(x-xx(3))*(x-xx(4))*(x-xx(5))*(x-xx(6))*(x-xx(7)))/...
        ((xx(1)-xx(2))*(xx(1)-xx(3))*(xx(1)-xx(4))*(xx(1)-xx(5))*(xx(1)-xx(6))*(xx(1)-xx(7)));
    L16=((x-xx(1))*(x-xx(3))*(x-xx(4))*(x-xx(5))*(x-xx(6))*(x-xx(7)))/...
        ((xx(2)-xx(1))*(xx(2)-xx(3))*(xx(2)-xx(4))*(xx(2)-xx(5))*(xx(2)-xx(6))*(xx(2)-xx(7)));
    L26=((x-xx(1))*(x-xx(2))*(x-xx(4))*(x-xx(5))*(x-xx(6))*(x-xx(7)))/...
        ((xx(3)-xx(1))*(xx(3)-xx(2))*(xx(3)-xx(4))*(xx(3)-xx(5))*(xx(3)-xx(6))*(xx(3)-xx(7)));
    L36=((x-xx(1))*(x-xx(2))*(x-xx(3))*(x-xx(5))*(x-xx(6))*(x-xx(7)))/...
        ((xx(4)-xx(1))*(xx(4)-xx(2))*(xx(4)-xx(3))*(xx(4)-xx(5))*(xx(4)-xx(6))*(xx(4)-xx(7)));
    L46=((x-xx(1))*(x-xx(2))*(x-xx(3))*(x-xx(4))*(x-xx(6))*(x-xx(7)))/...
        ((xx(5)-xx(1))*(xx(5)-xx(2))*(xx(5)-xx(3))*(xx(5)-xx(4))*(xx(5)-xx(6))*(xx(5)-xx(7)));
    L56=((x-xx(1))*(x-xx(2))*(x-xx(3))*(x-xx(4))*(x-xx(5))*(x-xx(7)))/...
        ((xx(6)-xx(1))*(xx(6)-xx(2))*(xx(6)-xx(3))*(xx(6)-xx(4))*(xx(6)-xx(5))*(xx(6)-xx(7)));
    L66=((x-xx(1))*(x-xx(2))*(x-xx(3))*(x-xx(4))*(x-xx(5))*(x-xx(6)))/...
        ((xx(7)-xx(1))*(xx(7)-xx(2))*(xx(7)-xx(3))*(xx(7)-xx(4))*(xx(7)-xx(5))*(xx(7)-xx(6)));
    L=L06*yy(1)+L16*yy(2)+L26*yy(3)+L36*yy(4)+L46*yy(5)+L56*yy(6)+L66*yy(7)
    yc=double(subs(L,x,xc))
    ezplot(L, [xx(1),xx(end)]);
    hold on
    plot(xx,yy,'bo')
end